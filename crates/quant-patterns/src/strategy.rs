//! # Strategy Pattern
//!
//! ## Financial Rationale
//!
//! A trading platform must support diverse strategies: trend following,
//! mean reversion, statistical arbitrage, factor models, ML-based, etc.
//! The Strategy pattern decouples the strategy interface from concrete
//! implementations, allowing strategies to be selected at runtime,
//! composed, and backtested uniformly.
//!
//! ## Rust Adaptation
//!
//! Rust's trait system is the natural fit for the Strategy pattern.
//! We define a `Strategy` trait with a single `evaluate` method.
//! Concrete strategies implement this trait. Object safety is maintained
//! by keeping the trait methods simple (no generics).
//!
//! ## Performance Note
//!
//! Using `&dyn Strategy` (trait objects) incurs a vtable dispatch cost.
//! For hot paths (e.g., HFT strategies), prefer `impl Strategy` or
//! monomorphized generics. This crate uses `&dyn` for flexibility in
//! backtesting engines where strategy selection is dynamic.

use std::fmt::Debug;

/// A market snapshot used as input to strategy evaluation.
///
/// In a real system this would contain order book depth, recent trades,
/// indicators, and alternative data. Here we keep it minimal for clarity.
#[derive(Debug, Clone, PartialEq)]
pub struct MarketContext {
    /// Current mid-price.
    pub price: f64,
    /// 24h volume.
    pub volume: f64,
    /// Timestamp (unix millis).
    pub timestamp: i64,
}

/// A trading signal generated by a strategy.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Signal {
    /// Enter long position.
    Long,
    /// Enter short position.
    Short,
    /// Flatten / exit position.
    Flat,
    /// No action.
    Hold,
}

/// Core strategy trait.
///
/// Implementors receive market context and emit a signal.
/// The trait is object-safe (no generic methods or `Self: Sized` bounds).
pub trait Strategy: Debug + Send + Sync {
    /// Human-readable strategy name.
    fn name(&self) -> &str;

    /// Evaluate the strategy given current market context.
    fn evaluate(&self, ctx: &MarketContext) -> Signal;

    /// Reset any internal state (useful for backtesting multiple runs).
    fn reset(&mut self);
}

// ------------------------------------------------------------------
// Concrete Strategy: Simple Moving Average Crossover
// ------------------------------------------------------------------

/// Moving Average Crossover strategy.
///
/// Generates a `Long` signal when the fast MA crosses above the slow MA,
/// and `Short` when it crosses below. Uses a simple ring buffer for
/// price history.
#[derive(Debug, Clone)]
pub struct MovingAverageCrossover {
    name: String,
    fast_period: usize,
    slow_period: usize,
    pub prices: Vec<f64>,
    last_signal: Signal,
}

impl MovingAverageCrossover {
    /// Create a new MA crossover strategy.
    ///
    /// # Panics
    ///
    /// Panics if `fast_period >= slow_period`.
    pub fn new(fast_period: usize, slow_period: usize) -> Self {
        assert!(
            fast_period < slow_period,
            "fast_period must be < slow_period"
        );
        Self {
            name: format!("MA_Crossover_{fast_period}_{slow_period}"),
            fast_period,
            slow_period,
            prices: Vec::with_capacity(slow_period),
            last_signal: Signal::Hold,
        }
    }

    fn fast_ma(&self) -> f64 {
        let window = self.prices.iter().rev().take(self.fast_period);
        let sum: f64 = window.sum();
        sum / self.fast_period as f64
    }

    fn slow_ma(&self) -> f64 {
        let window = self.prices.iter().rev().take(self.slow_period);
        let sum: f64 = window.sum();
        sum / self.slow_period as f64
    }
}

impl Strategy for MovingAverageCrossover {
    fn name(&self) -> &str {
        &self.name
    }

    fn evaluate(&self, _ctx: &MarketContext) -> Signal {
        if self.prices.len() < self.slow_period {
            return Signal::Hold;
        }
        let fast = self.fast_ma();
        let slow = self.slow_ma();

        if fast > slow && self.last_signal != Signal::Long {
            return Signal::Long;
        }
        if fast < slow && self.last_signal != Signal::Short {
            return Signal::Short;
        }
        Signal::Hold
    }

    fn reset(&mut self) {
        self.prices.clear();
        self.last_signal = Signal::Hold;
    }
}

