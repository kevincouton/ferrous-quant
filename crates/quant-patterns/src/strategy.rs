// Concrete Strategy: Mean Reversion (Bollinger Band touch)
// ------------------------------------------------------------------

/// Simple mean-reversion strategy based on z-score threshold.
#[derive(Debug, Clone)]
pub struct MeanReversion {
    name: String,
    lookback: usize,
    z_threshold: f64,
    prices: Vec<f64>,
}

impl MeanReversion {
    /// Create a new mean reversion strategy.
    ///
    /// `z_threshold` is the number of standard deviations from the mean
    /// that triggers a signal.
    pub fn new(lookback: usize, z_threshold: f64) -> Self {
        Self {
            name: format!("MeanReversion_{lookback}_{z_threshold}"),
            lookback,
            z_threshold,
            prices: Vec::with_capacity(lookback),
        }
    }
}

impl Strategy for MeanReversion {
    fn name(&self) -> &str {
        &self.name
    }

    fn evaluate(&self, ctx: &MarketContext) -> Signal {
        if self.prices.len() < self.lookback {
            return Signal::Hold;
        }
        let mean = self.prices.iter().sum::<f64>() / self.prices.len() as f64;
        let variance =
            self.prices.iter().map(|p| (p - mean).powi(2)).sum::<f64>() / self.prices.len() as f64;
        let std = variance.sqrt();
        if std == 0.0 {
            return Signal::Hold;
        }
        let z = (ctx.price - mean) / std;

        if z < -self.z_threshold {
            Signal::Long // Price is low, expect reversion up
        } else if z > self.z_threshold {
            Signal::Short // Price is high, expect reversion down
        } else {
            Signal::Hold
        }
    }

    fn reset(&mut self) {
        self.prices.clear();
    }
}

// ------------------------------------------------------------------
// Strategy Registry (Factory + Strategy combined)
// ------------------------------------------------------------------

use std::collections::HashMap;

/// Registry of named strategies. Allows runtime selection.
#[derive(Default)]
pub struct StrategyRegistry {
    strategies: HashMap<String, Box<dyn Strategy>>,
}

impl StrategyRegistry {
    /// Create an empty registry.
    pub fn new() -> Self {
        Self::default()
    }

    /// Register a strategy under a name.
    pub fn register(&mut self, name: impl Into<String>, strategy: Box<dyn Strategy>) {
        self.strategies.insert(name.into(), strategy);
    }

    /// Retrieve a strategy by name.
    pub fn get(&self, name: &str) -> Option<&dyn Strategy> {
        self.strategies.get(name).map(|b| b.as_ref())
    }

    /// Evaluate all registered strategies and return their signals.
    pub fn evaluate_all(&self, ctx: &MarketContext) -> Vec<(&str, Signal)> {
        self.strategies
            .iter()
            .map(|(name, strat)| (name.as_str(), strat.evaluate(ctx)))
            .collect()
    }
}

// ------------------------------------------------------------------
// Tests
// ------------------------------------------------------------------

#[cfg(test)]
mod tests {
    use super::*;

    fn ctx(price: f64) -> MarketContext {
        MarketContext {
            price,
            volume: 1_000_000.0,
            timestamp: 0,
        }
    }

    #[test]
    fn ma_crossover_generates_signals() {
        let mut strat = MovingAverageCrossover::new(2, 5);
        // Seed prices: uptrend (fast will be above slow)
        for i in 1..=5 {
            strat.prices.push(i as f64);
        }
        strat.last_signal = Signal::Short; // Force change
        let signal = strat.evaluate(&ctx(6.0));
        assert_eq!(signal, Signal::Long);
    }

    #[test]
    fn mean_reversion_buy_on_low_z() {
        let mut strat = MeanReversion::new(5, 1.5);
        // Prices with variance centered around 100
        for i in 0..5 {
            strat.prices.push(90.0 + i as f64 * 5.0); // 90, 95, 100, 105, 110
        }
        let signal = strat.evaluate(&ctx(80.0)); // well below mean
        assert_eq!(signal, Signal::Long);
    }

    #[test]
    fn mean_reversion_sell_on_high_z() {
        let mut strat = MeanReversion::new(5, 1.5);
        for i in 0..5 {
            strat.prices.push(90.0 + i as f64 * 5.0); // 90, 95, 100, 105, 110
        }
        let signal = strat.evaluate(&ctx(120.0)); // well above mean
        assert_eq!(signal, Signal::Short);
    }

    #[test]
    fn registry_evaluates_multiple() {
        let mut reg = StrategyRegistry::new();
        reg.register("ma", Box::new(MovingAverageCrossover::new(2, 5)));
        reg.register("mr", Box::new(MeanReversion::new(5, 1.5)));

        let results = reg.evaluate_all(&ctx(100.0));
        assert_eq!(results.len(), 2);
    }
}
